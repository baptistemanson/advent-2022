pub fn pb1() {
    let mut score: u32 = 0;
    let x = 1;
    let y = 2;
    let z = 3;
    let loose = 0;
    let draw = 3;
    let win = 6;
    for line in INPUT.lines() {
        score += match line {
            "A X" => x + draw,
            "A Y" => y + win,
            "A Z" => z + loose,
            "B X" => x + loose,
            "B Y" => y + draw,
            "B Z" => z + win,
            "C X" => x + win,
            "C Y" => y + loose,
            "C Z" => z + draw,
            _ => panic!("unknown combination"),
        };
    }
    println!("{}", score);
}

pub fn pb2() {
    let mut score: u32 = 0;
    let x = 0;
    let y = 3;
    let z = 6;
    let a = 1; // rock
    let b = 2; // paper
    let c = 3; // scissor
    for line in INPUT.lines() {
        score += match line {
            "A X" => x + c, // Rock > Scissor
            "A Y" => y + a,
            "A Z" => z + b,
            "B X" => x + a, // Paper > Rock
            "B Y" => y + b,
            "B Z" => z + c,
            "C X" => x + b, // Scissor > Paper
            "C Y" => y + c,
            "C Z" => z + a,
            _ => panic!("unknown combination"),
        };
    }
    println!("{}", score);
}

pub fn pb2bis() {
    let mut score: u32 = 0;
    for line in INPUT.lines() {
        let mut c = line.chars();
        score += get_play_score(c.nth(0).unwrap().into(), c.nth(2).unwrap().into())
    }
    println!("{}", score);
}

enum Play {
    Rock = 1,
    Paper = 2,
    Scissors = 3,
}

impl From<char> for Play {
    fn from(c: char) -> Self {
        match c {
            'A' => Play::Rock,
            'B' => Play::Paper,
            'C' => Play::Scissors,
            _ => panic!("invalid option"),
        }
    }
}
enum Score {
    Loose = 0,
    Draw = 3,
    Win = 6,
}

impl From<char> for Score {
    fn from(c: char) -> Self {
        match c {
            'X' => Score::Loose,
            'Y' => Score::Draw,
            'Z' => Score::Win,
            _ => panic!("invalid option"),
        }
    }
}

fn get_play_score(play: Play, score: Score) -> u32 {
    return match score {
        Score::Loose => {
            Score::Loose as u32
                + match play {
                    Play::Rock => Play::Scissors,
                    Play::Paper => Play::Rock,
                    Play::Scissors => Play::Paper,
                } as u32
        }
        Score::Draw => Score::Draw as u32 + play as u32,
        Score::Win => {
            Score::Win as u32
                + match play {
                    Play::Rock => Play::Paper,
                    Play::Paper => Play::Scissors,
                    Play::Scissors => Play::Rock,
                } as u32
        }
    };
}

